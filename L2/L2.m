% Для очищення простору
clear;
close all;
clc;

% Завдання 1-2
% Завантаження та відображення стандартних зображень
img1 = imread('eight.tif'); 
img2 = imread('moon.tif');  
img3 = imread('cameraman.tif');

figure, imshow(img1), title('eight.tif');
figure, imshow(img2), title('moon.tif');
figure, imshow(img3), title('cameraman.tif');

% Завдання 3
% Здійснити процедуру зашумлення зображення нормальним білим
% шумом і імпульсною перешкодою з різною щільністю.

J_gauss = imnoise(img1,'gaussian',0,0.01);
J_sp1 = imnoise(img1,'salt & pepper',0.02);
J_sp2 = imnoise(img1,'salt & pepper',0.08);

% Завдання 4
% Відобразити зашумлені зображення.

figure,imshow(J_gauss),title('Зображення з гаусівським шумом');
figure,imshow(J_sp1),title('Salt & Pepper шум (0.02)');
figure,imshow(J_sp2),title('Salt & Pepper шум (0.08)');

% Завдання 5
% Виконати фільтрацію зображень лінійними фільтрами.
h_low = ones(3,3)/9;
I_low = imfilter(J_gauss,h_low);

h_high = [0 -1 0; -1 5 -1; 0 -1 0];
I_high = imfilter(img1,h_high);

% Завдання 6
% Відобразити зображення після фільтрації.
figure,imshow(I_low),title('Після низькочастотної фільтрації');
figure,imshow(I_high),title('Після високочастотної фільтрації');

% Завдання 7
% Профільтрувати зашумлені зображення різними лінійними фільтрами.
I_sp_low = imfilter(J_sp1,h_low);
figure,imshow(I_sp_low),title('Фільтрація salt & pepper шуму');

% Завдання 8
% Виконати фільтрацію адаптивним вінерівським фільтром.
I_wiener = wiener2(J_gauss,[5 5]);
figure,imshow(I_wiener),title('Вінерівська фільтрація');

% Завдання 9
% Виконати фільтрацію медіанним фільтром.
I_median = medfilt2(J_sp1);
figure,imshow(I_median),title('Медіанна фільтрація');

