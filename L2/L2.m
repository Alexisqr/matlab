% 1.	Поясніть процедуру лінійної фільтрації зображення віконним фільтром.

% Лінійна фільтрація виконується шляхом переміщення спеціального маски по всьому
% зображенню. Для кожного пікселя обчислюється нове значення як зважена сума значень 
% сусідніх пікселів, які потрапляють у це вікно. Вагові коефіцієнти визначаються
%  елементами маски фільтра.


% 2.	Чому відрізняються "вікна фільтрів" для низькочастотної і високочастотної фільтрації?

% Вікна відрізняються, тому що вони виконують різні задачі. Низькочастотні фільтри усереднюють
% значення пікселів і згладжують зображення, тому їх маски містять додатні значення.
% Високочастотні фільтри підсилюють різкі перепади яскравості, тому їх маски мають
% як додатні, так і від'ємні значення.


% 3.	Як змінюється вигляд зображення після його фільтрації низькочастотними фільтрами?

% Після такої фільтрації зображення стає більш згладженим, зменшується кількість шуму,
% але дрібні деталі та різкі контури можуть трохи розмиватися.


% 4.	Як змінюється вигляд зображення після його фільтрації високочастотними фільтрами?

% Після високочастотної фільтрації контури об'єктів і дрібні деталі стають більш чіткими.
%  Однак при цьому може також підсилюватися шум на зображенні.


% 5.	Для перешкод якого характеру більше підходить медіанна фільтрація, і чому?

% Медіанна фільтрація найкраще підходить для імпульсних перешкод, наприклад шуму типу
% «salt & pepper». Це тому, що медіанний фільтр замінює значення пікселя медіанним значенням
% сусідніх пікселів і добре видаляє поодинокі дуже яскраві або дуже темні точки.
